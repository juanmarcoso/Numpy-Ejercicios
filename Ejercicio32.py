# Extract all elements from the following array with a value greater than 8 and print result to the console.
#     A = np.array(
#         [[4.99, 3.49, 9.99], [1.99, 9.99, 4.99], [14.99, 2.39, 7.29]]
#     )
# Expected result:
#     [ 9.99  9.99 14.99]

import numpy as np


